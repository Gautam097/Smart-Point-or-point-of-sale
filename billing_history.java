    public class mytablemodel extends AbstractTableModel {

        @Override
        public int getRowCount() {
            return al.size();
        }

        @Override
        public int getColumnCount() {
            return 6;
        }

        @Override
        public String getColumnName(int j) {
            String b[] = {"bill_id", "date time", "total price", "username", "customer no", "payment type"};
            return b[j];
        }

        @Override
        public Object getValueAt(int i, int j) {
            bill b = al.get(i);
            if (j == 0) {
                return b.bill_id;
            } else if (j == 1) {
                return b.date_time;
            } else if (j == 2) {
                return b.total_price;
            } else if (j == 3) {
                return b.username;
            } else if (j == 4) {
                return b.customer_no;
            } else {
                return b.payment_method;
            }
        }

    }

