    void viewHistory() {
        try {
            al.clear();
            ResultSet rs = DBLoader.exexuteSQL("select * from bill where username='"+global.admin_name+"' ");
            while (rs.next()) {
                int bill_id = rs.getInt("bill_id");
                String date_time = rs.getString("date_time");
                int total_price = rs.getInt("total_price");
                String username = rs.getString("username");
                String customer_no = rs.getString("customer_no");
                String payment_type = rs.getString("payment_type");
                al.add(new bill(bill_id, date_time, total_price, username, customer_no, payment_type));
            }
            tm.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

